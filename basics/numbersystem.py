# Type Casting : Converting between different number system , Hex -> Decimal, Octal -> Decimal

octal=0o20 # decimal value 8
binary=0b10000 # decimal value 16
hexa=0x10 # decimal 16

print("Octal of ,",oct(octal)," is ->",octal)
print("binary of, ",bin(binary)," is ->", binary)
print("Hex of, ",hex(hexa),"is ->",hexa)

